// BASIC THEORY 
// ✅ What is AJAX?
// AJAX stands for:
// A - Asynchronous
// J - JavaScript
// A - And
// X - XML
//
// 🔸 Note: Nowadays, JSON is more commonly used instead of XML.

// 💡 AJAX Meaning:
// AJAX is a technique in JavaScript used to send and receive data from a server
// without reloading the entire webpage.

// 🔍 Why is AJAX used?
// - To update parts of a webpage without reloading the whole page.
// - To send or get data from a server in the background.
// - To create faster, smoother, and more dynamic websites.

// 📦 Example Use-Cases:
// - Google search suggestions.
// - Instagram feed loading as you scroll.
// - Submitting a form without page reload.

// ✅ Summary:
// AJAX allows you to fetch or send data to a server using JavaScript,
// without refreshing the webpage.

// 💡 You can use AJAX via:
// 1.) xmlHTTPRequest (old way of doing)
// 2.) fetch API (new way of doing)
// 3.) axios (this is third party library)
